public class Variables {
    public static void main(String[] args) {
        //HomeWork
        //Variables:char
        char initialName = 65;
        char initialSurname = 77;
        System.out.println(initialName);
        System.out.println(initialSurname);

        //Variables:int
        int age = 24;
        int ageNextYear = age + 1;
        System.out.println(age);
        System.out.println(ageNextYear);

        //Variables: float
        float dateOfBirth = 21.03F;
        float eyeSight = -1.5f;
        System.out.println(dateOfBirth);
        System.out.println(eyeSight);

        //Variables : double
        double weight = 91.7;
        double height = 188.1;
        System.out.println(weight);
        System.out.println(height);

        //Variables : booljon
        boolean isArtjomsCoach = true;
        boolean isArtjomsTester = false;
        System.out.println(isArtjomsCoach);
        System.out.println(isArtjomsTester);

        //Variables : long
        long expNeeded = 1000000000;
        long expNow = 999999999;
        System.out.println(expNeeded);
        System.out.println(expNow);
    }
}
